def square(x, y):
    """Dibuja un cuadrado en la posición indicada."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Obtiene la posición de un punto dentro de la lista de tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)

    return index


def valid(point):
    """Verifica si el movimiento es válido."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Dibuja el mapa del juego."""
    bgcolor('black')

    # Cambio realizado: el mapa ahora es morado
    path.color('purple')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20

            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Mueve a Pacman y a los fantasmas."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1

        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20

        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)

        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]

            plan = choice(options)

            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)

        # Cambio realizado: fantasmas verdes
        dot(20, 'green')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Cambia la dirección de movimiento de Pacman."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


